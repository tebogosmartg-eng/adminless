"use client";

import { useState, useEffect, useRef } from 'react';
import { Dialog, DialogContent, DialogDescription, DialogHeader, DialogTitle } from '@/components/ui/dialog';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { ScrollArea } from '@/components/ui/scroll-area';
import { Textarea } from '@/components/ui/textarea';
import { Badge } from '@/components/ui/badge';
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table';
import { 
    Download, 
    Loader2, 
    AlertTriangle, 
    Save,
    CheckCircle2,
    Plus,
    Trash2,
    Table as TableIcon,
    BrainCircuit,
    Info
} from 'lucide-react';
import { Assessment, Learner, DiagnosticRow } from '@/lib/types';
import { useQuestionAnalysis } from '@/hooks/useQuestionAnalysis';
import { useSettings } from '@/context/SettingsContext';
import { generateQuestionDiagnosticPDF } from '@/utils/pdf/questionDiagnosticReport';
import { showSuccess, showError } from '@/utils/toast';
import { cn } from '@/lib/utils';

interface QuestionDiagnosticDialogProps {
  open: boolean;
  onOpenChange: (open: boolean) => void;
  assessment: Assessment;
  learners: Learner[];
}

export const QuestionDiagnosticDialog = ({ open, onOpenChange, assessment, learners }: QuestionDiagnosticDialogProps) => {
  const { stats, loading, saveDiagnostic, generateAIAnalysis } = useQuestionAnalysis(assessment, learners);
  const { schoolName, teacherName, schoolLogo, contactEmail, contactPhone } = useSettings();
  
  const [rows, setRows] = useState<DiagnosticRow[]>([]);
  const [isExporting, setIsExporting] = useState(false);
  const [isSaving, setIsSaving] = useState(false);
  const [isGeneratingAI, setIsGeneratingAI] = useState(false);
  
  const initializedRef = useRef(false);

  useEffect(() => {
    if (stats && !initializedRef.current) {
        let loadedRows: DiagnosticRow[] = [];
        
        if (stats.savedDiagnostic?.findings) {
            try {
                const parsed = JSON.parse(stats.savedDiagnostic.findings);
                if (Array.isArray(parsed)) {
                    loadedRows = parsed;
                } else {
                    loadedRows = stats.initialRows;
                }
            } catch (e) {
                loadedRows = stats.initialRows;
            }
        } else {
            loadedRows = stats.initialRows;
        }

        setRows(loadedRows);
        initializedRef.current = true;
    }
  }, [stats]);

  useEffect(() => {
      if (!open) {
          initializedRef.current = false;
      }
  }, [open, assessment.id]);

  const handleUpdateRow = (id: string, field: keyof DiagnosticRow, value: any) => {
      setRows(prev => prev.map(r => r.id === id ? { ...r, [field]: value } : r));
  };

  const handleAddRow = () => {
      setRows(prev => [...prev, {
          id: crypto.randomUUID(),
          question: "Manual Item",
          performance_summary: "",
          possible_root_causes: [],
          targeted_interventions: []
      }]);
  };

  const handleDeleteRow = (id: string) => {
      setRows(prev => prev.filter(r => r.id !== id));
  };

  const handleSave = async () => {
    setIsSaving(true);
    await saveDiagnostic(rows);
    setIsSaving(false);
  };

  const handleRunAI = async () => {
      setIsGeneratingAI(true);
      try {
          const classMeta = learners[0]?.class_id ? await (async () => {
              const cls = await (window as any).db.classes.get(learners[0].class_id);
              return { subject: cls?.subject, grade: cls?.grade };
          })() : { subject: "General", grade: "N/A" };

          const aiRows = await generateAIAnalysis(classMeta.subject, classMeta.grade);
          if (aiRows) {
              setRows(aiRows);
              showSuccess("Root-cause analysis generated by AI.");
          }
      } finally {
          setIsGeneratingAI(false);
      }
  };

  const handleExport = () => {
    if (!stats) return;
    setIsExporting(true);
    try {
        generateQuestionDiagnosticPDF(
            assessment,
            learners,
            stats.qStats,
            stats.rawMarks,
            rows,
            { name: schoolName, teacher: teacherName, logo: schoolLogo, email: contactEmail, phone: contactPhone }
        );
        showSuccess("Detailed diagnostic report exported.");
    } finally {
        setIsExporting(false);
    }
  };

  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="max-w-[95vw] w-[1200px] h-[90vh] flex flex-col p-0 overflow-hidden">
        <div className="p-6 pb-4 border-b bg-muted/20 shrink-0">
          <DialogHeader>
            <div className="flex justify-between items-start">
                <div className="space-y-1">
                    <div className="flex items-center gap-2">
                        <Badge variant="outline" className="bg-primary/5 text-primary border-primary/20 uppercase tracking-widest text-[9px] font-black">
                            Root-Cause Diagnostic
                        </Badge>
                        {stats?.savedDiagnostic && (
                            <Badge variant="secondary" className="bg-green-100 text-green-700 border-none text-[9px] font-black uppercase">
                                <CheckCircle2 className="h-2.5 w-2.5 mr-1" /> Document Finalized
                            </Badge>
                        )}
                    </div>
                    <DialogTitle className="text-2xl font-bold">{assessment.title}</DialogTitle>
                    <DialogDescription>Identify pedagogical barriers and targeted remediation strategies.</DialogDescription>
                </div>
                <div className="flex gap-2">
                    <Button variant="outline" onClick={handleRunAI} disabled={isGeneratingAI || loading} className="gap-2 font-bold h-9 bg-purple-50 text-purple-700 border-purple-200 hover:bg-purple-100">
                        {isGeneratingAI ? <Loader2 className="h-4 w-4 animate-spin" /> : <BrainCircuit className="h-4 w-4" />}
                        Generate AI Analysis
                    </Button>
                    <Button variant="outline" onClick={handleSave} disabled={isSaving || loading} className="gap-2 font-bold h-9">
                        {isSaving ? <Loader2 className="h-4 w-4 animate-spin" /> : <Save className="h-4 w-4" />}
                        Save Changes
                    </Button>
                    <Button onClick={handleExport} disabled={isExporting || !stats} className="font-bold gap-2 h-9">
                        {isExporting ? <Loader2 className="h-4 w-4 animate-spin" /> : <Download className="h-4 w-4" />}
                        Export PDF
                    </Button>
                </div>
            </div>
          </DialogHeader>
        </div>

        <ScrollArea className="flex-1 p-6">
          {loading ? (
            <div className="flex flex-col items-center justify-center py-20 gap-4">
                <Loader2 className="h-10 w-10 animate-spin text-primary opacity-20" />
                <p className="text-sm font-medium text-muted-foreground animate-pulse">Running data heuristics...</p>
            </div>
          ) : stats ? (
            <div className="space-y-10 pb-10">
                <div className="grid grid-cols-2 md:grid-cols-5 gap-4">
                    {stats.qStats.map(s => (
                        <div key={s.id} className={cn(
                            "flex flex-col p-4 rounded-xl border transition-all relative overflow-hidden",
                            s.isWeak ? "bg-red-50 border-red-100" : "bg-muted/30 border-muted-foreground/10"
                        )}>
                            <div className="flex justify-between items-start mb-2 relative z-10">
                                <span className="text-[10px] font-black uppercase tracking-widest text-muted-foreground">Q{s.number}</span>
                                {s.isWeak ? <AlertTriangle className="h-3 w-3 text-red-500" /> : <CheckCircle2 className="h-3 w-3 text-green-600" />}
                            </div>
                            <div className="text-2xl font-black relative z-10">{s.avg}%</div>
                            <div className="flex justify-between items-center mt-1 relative z-10">
                                <p className="text-[9px] text-muted-foreground uppercase font-bold truncate max-w-[80px]" title={s.skill}>{s.skill || "Skill"}</p>
                                <span className="text-[8px] font-black text-muted-foreground/60">{s.passRate}% Pass</span>
                            </div>
                        </div>
                    ))}
                </div>

                <div className="space-y-4">
                    <div className="flex items-center justify-between">
                        <div className="flex items-center gap-2">
                            <TableIcon className="h-4 w-4 text-primary" />
                            <h4 className="text-xs font-black uppercase tracking-widest text-muted-foreground">Diagnostic Intervention Grid</h4>
                        </div>
                        <Button variant="outline" size="sm" onClick={handleAddRow} className="h-7 text-[10px] font-bold uppercase">
                            <Plus className="h-3 w-3 mr-1" /> Add Row
                        </Button>
                    </div>

                    <div className="border rounded-xl overflow-hidden bg-background shadow-sm">
                        <Table>
                            <TableHeader className="bg-muted/50">
                                <TableRow>
                                    <TableHead className="w-[15%] font-black text-[9px] uppercase tracking-widest py-3">Question</TableHead>
                                    <TableHead className="w-[20%] font-black text-[9px] uppercase tracking-widest py-3">Performance Summary</TableHead>
                                    <TableHead className="w-[30%] font-black text-[9px] uppercase tracking-widest py-3">Root Causes (Possible)</TableHead>
                                    <TableHead className="w-[30%] font-black text-[9px] uppercase tracking-widest py-3">Targeted Interventions</TableHead>
                                    <TableHead className="w-[5%]"></TableHead>
                                </TableRow>
                            </TableHeader>
                            <TableBody>
                                {rows.map((row) => (
                                    <TableRow key={row.id} className="group hover:bg-muted/5 transition-colors align-top">
                                        <TableCell className="p-3">
                                            <Input 
                                                value={row.question}
                                                onChange={(e) => handleUpdateRow(row.id, 'question', e.target.value)}
                                                className="border-none shadow-none font-bold text-sm bg-transparent p-0 focus-visible:ring-0"
                                            />
                                        </TableCell>
                                        <TableCell className="p-3">
                                            <Textarea 
                                                value={row.performance_summary}
                                                onChange={(e) => handleUpdateRow(row.id, 'performance_summary', e.target.value)}
                                                className="border-none shadow-none resize-none bg-transparent min-h-[60px] text-xs p-0 focus-visible:ring-0"
                                            />
                                        </TableCell>
                                        <TableCell className="p-3 border-l">
                                            <Textarea 
                                                value={row.possible_root_causes.join('\n')}
                                                onChange={(e) => handleUpdateRow(row.id, 'possible_root_causes', e.target.value.split('\n'))}
                                                className="border-none shadow-none resize-none bg-transparent min-h-[100px] text-xs p-0 focus-visible:ring-0 leading-relaxed"
                                                placeholder="Enter causes (one per line)..."
                                            />
                                        </TableCell>
                                        <TableCell className="p-3 border-l">
                                            <Textarea 
                                                value={row.targeted_interventions.join('\n')}
                                                onChange={(e) => handleUpdateRow(row.id, 'targeted_interventions', e.target.value.split('\n'))}
                                                className="border-none shadow-none resize-none bg-transparent min-h-[100px] text-xs p-0 focus-visible:ring-0 leading-relaxed text-blue-800 dark:text-blue-300 font-medium"
                                                placeholder="Enter actions (one per line)..."
                                            />
                                        </TableCell>
                                        <TableCell className="p-2 text-right">
                                            <Button 
                                                variant="ghost" 
                                                size="icon" 
                                                className="h-7 w-7 text-muted-foreground hover:text-destructive opacity-0 group-hover:opacity-100 transition-opacity"
                                                onClick={() => handleDeleteRow(row.id)}
                                            >
                                                <Trash2 className="h-3.5 w-3.5" />
                                            </Button>
                                        </TableCell>
                                    </TableRow>
                                ))}
                            </TableBody>
                        </Table>
                    </div>
                </div>

                <div className="p-4 bg-blue-50 border border-blue-100 rounded-xl flex items-start gap-3">
                    <Info className="h-5 w-5 text-blue-600 mt-0.5" />
                    <div className="space-y-1">
                        <p className="text-xs font-bold text-blue-900 uppercase tracking-tight">Pro-Tip: Multi-line Input</p>
                        <p className="text-[11px] text-blue-800 leading-tight">
                            Type each root cause or intervention on a <strong>new line</strong> in the text boxes above. The system will automatically format them as bullet points in your professional PDF export.
                        </p>
                    </div>
                </div>
            </div>
          ) : null}
        </ScrollArea>
      </DialogContent>
    </Dialog>
  );
};